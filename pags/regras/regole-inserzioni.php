                    Puoi inserire annunci relativi a prodotti in stock solo se venduti in blocco ad un unico prezzo; <br>
                    Il testo degli annunci deve essere dettagliato e relativo all’oggetto dell’annuncio. <br>
                    Non è permesso l'inserimento di frasi generiche, non riferite al prodotto in vendita o che promuovano servizi non direttamente riferibili all'annuncio Tutti i prodotti e servizi offerti devono essere disponibili sul territorio italiano.
