        <p>L'editore potrà limitare il numero di annunci pubblicabili da una singola persona </p>
        <br>
        <div class="texto">
            <ol>
                <li>
                    <p>Al raggiungimento di soglie predeterminate di inserimento degli annunci, variabili in base alla categoria di inserimento, l'utente non potrà inserire ulteriori annunci</p>
                    <ul>
                        <li>
                            <p>(artisti 4 opere)</p>
                        </li>
                    </ul>
                    <ul>
                        <li>
                            <p>(formatori maestri 8 opere)</p>
                        </li>
                    </ul>
