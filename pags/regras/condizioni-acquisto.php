        <h1 class="titulo">Condizioni acquisto</h1>
        <div class="texto">
            <ol>
                <li>
                    <h3>IL PAGAMENTO IN EUROPA</h3>
                    <p>
                        Con le  norme dell’UE i pagamenti elettronici diventano meno costosi, più facili e più sicuri. Ecco come: <br>
                        puoi effettuare pagamenti in tutta Europa (UE, Islanda, Norvegia e Liechtenstein) con la stessa semplicità e sicurezza dei pagamenti nel tuo paese.
                    </p>
                    <br>
                    <p>
                        Gli esercenti non possono più addebitarti costi aggiuntivi se paghi con una carta emessa nell’UE. Le norme riguardano tutti i tipi di pagamenti elettronici (ad esempio bonifici, addebiti diretti, pagamenti tramite carta...).
                        Chiunque soggiorni legalmente in Europa ha diritto a un conto bancario per effettuare pagamenti elettronici Puoi godere di questi diritti grazie alla revisione della direttiva UE sui servizi di pagamento (PSD2), alla direttiva sui conti di pagamento e ad altre normative dell’UE volte a rendere i pagamenti più sicuri e più semplici.
                    </p>
                    <br>
                    <p>
                        Per saperne di più sui tuoi diritti: 
                        <span>
                            <a href="https://europa.eu/youreurope/citizens/consumers/financial-products-and-services/index_it.htm" target="_blank">Prodotti e servizi finanziari</a>
                        </span>
                    </p>
                </li>

                <li>
                    <h3>PAGAMENTI ELETTRONICI ACCESSIBILI A TUTTI</h3>
                    <ul>
                        <li>
                            <p>Ogni consumatore che soggiorna legalmente nell’UE ha diritto, gratuitamente o a un prezzo ragionevole, almeno a un conto di pagamento di base (vale a dire un conto che prevede una carta di debito e consente il prelievo dei contanti, la custodia dei fondi e l’esecuzione e la ricezione di pagamenti).</p>
                        </li>
                    </ul>
                </li>

                <li>
                    <h3>PAGAMENTI IN EURO - STESSI COSTI DI UN PAGAMENTO LOCALE</h3>
                    <ul>
                        <li>
                            <p>Un unico conto in euro ti consente di effettuare qualsiasi pagamento in tutta Europa.</p>
                        </li>
                        <li>
                            <p>I pagamenti transfrontalieri in euro avranno lo stesso costo dei pagamenti nazionali in euro, e dal 15 dicembre 2019 i pagamenti transfrontalieri in euro avranno lo stesso costo dei pagamenti nazionali nella tua valuta nazionale.</p>
                        </li>
                        <li>
                            <p>I prelievi di contanti in euro agli sportelli bancomat che non appartengono alla tua banca dovrebbero avere lo stesso costo, sia nel tuo paese che in un altro S tato membro.</p>
                        </li>
                    </ul>
                </li>

                <li>
                    <h3>PIU' SICUREZZA E MIGLIORE PROTEZIONE</h3>
                    <p>
                        I pagamenti elettronici saranno più sicuri grazie all’autenticazione forte del cliente. Questo sistema si basa su una combinazione di diversi fattori di autenticazione, ad esempio un PIN e l’impronta digitale. Per ulteriori informazioni su questo argomento contatta la tua banca o il tuo prestatore di servizi di pagamento.
                    </p>
                    <br>
                    <ul>
                        <li>
                            <p>Se viene effettuato un pagamento non autorizzato, ad esempio in caso di furto della tua carta di credito, la tua responsabilità è limitata a un massimo di 50 € (salvi i casi di colpa grave). Non sarai responsabile dei pagamenti non autorizzati effettuati dopo che avrai informato la banca né dei pagamenti online se il tuo prestatore di servizi di pagamento o la tua banca non prevedono l’autenticazione forte del cliente.</p>
                        </li>
                        <li>
                            <p>Se l’importo finale del pagamento tramite carta non è noto in anticipo l’esercente può bloccare un importo concordato sulla tua carta solo con il tuo consenso.</p>
                        </li>
                        <li>
                            <p>Con l’”addebito diretto” (ad esempio se hai autorizzato una società a riscuotere i pagamenti dal tuo conto) hai 8 settimane per contestare qualsiasi importo addebitato indebitamente. Il rimborso deve avvenire entro 10 giorni lavorativi.</p>
                        </li>
                    </ul>
                </li>

                <li>
                    <h3>COSTI EQUI</h3>
                    <ul>
                        <li>
                            <p>Hai il diritto di sapere se ai tuoi pagamenti si applicano delle spese e quali sono.</p>
                        </li>
                        <li>
                            <p>Di norma gli esercenti, sia nei negozi che online, non possono farti pagare più del prezzo pubblicato (“maggiorazione”) in caso di pagamento con carte di credito e di debito ad uso dei consumatori. In certi casi (ad esempio per alcune carte) può ancora essere applicata una maggiorazione, ma deve riflettere il costo reale per l’esercente di questo metodo di pagamento. Se ritieni che ti siano stati applicati costi eccessivi, ad esempio per la prenotazione di un volo o il pagamento di un albergo, consulta il link riportato alla pagina seguente per sapere di più sui tuoi diritti.</p>
                        </li>
                    </ul>
                </li>

                <li>
                    <h3>NUOVI SERVIZI</h3>
                    <ul>
                        <li>
                            <p>Grazie alle moderne tecnologie oggi puoi utilizzare servizi finanziari innovativi che possono essere offerti da banche e da altri prestatori di servizi di pagamento. Ciò significa, ad esempio, che puoi monitorare le tue finanze personali o effettuare acquisti online senza una carta di credito o di debito. Proprio come le banche, questi nuovi prestatori di servizi di pagamento devono essere autorizzati e sono soggetti a vigilanza, e devono gestire i tuoi dati in modo sicuro.</p>
                        </li>
                        <br>
                        <p>
                            Le norme dell’UE garantiscono la corretta esecuzione dei tuoi pagamenti elettronici. Ma se si verifica un problema, la banca o gli altri prestatori di servizi di pagamento devono rispondere ai reclami entro 15 giorni lavorativi. Se non sei ancora soddisfatto, puoi rivolgerti all’autorità nazionale competente.
                        </p>
                    </ul>
                </li>
                
            </ol>
