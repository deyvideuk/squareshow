        <h1 class="titulo">REGOLE ANNUNCI</h1>
        <div class="texto">
            <p>
                Oltre a quanto fin qui previsto, ti ricordiamo che gli annunci devono rispettare tutte le leggi e i regolamenti vigenti in Italia. <br> Gli annunci vengono pubblicati sempre sotto la sola responsabilità dell'utente, che all'atto dell'inserimento dell'annuncio dichiara altresì di conoscere e accettare i termini d'uso del servizio. <br> Subito non potrà essere ritenuta responsabile ad alcun titolo per quanto pubblicato dagli utenti e per la loro condotta. Ci riserviamo il diritto di:
            </p>
            <ul>
                <li>
                    <p>decidere in qualunque momento le azioni da compiere in caso di annunci che infrangano le presenti regole o i principi su cui Subito si basa.</p>
                </li>
                <li>
                    <p>modificare/aggiornare le regole in qualunque momento, anche senza alcun preavviso.</p>
                </li>
                <li>
                    <p>modificare il titolo degli annunci per ragioni editoriali e/o per rendere più comprensibile il contenuto dell'annuncio stesso.</p>
                </li>
                <li>
                    <p>non pubblicare un annuncio o, se pubblicato direttamente on line dall'utente, di cancellarlo, qualora il contenuto dell'annuncio violi le condizioni generali di utilizzo del servizio, e/o le presenti regole o, in ogni caso, qualora il suo contenuto non sia conforme alla politica editoriale. Resta inteso che la piattaforma web non sarà comunque responsabile di eventuali danni diretti e/o indiretti arrecati a terzi dalla pubblicazione dell'annuncio, anche qualora non abbia disposto la sua cancellazione.</p>
                </li>
            </ul>
            <p>
                Ti ricordiamo che collaboriamo con le forze dell'ordine nel caso di annunci che infrangano la legge. L'identità di inserzionisti o compratori può essere determinata tramite il provider di servizi internet. Ad esempio, gli indirizzi IP potrebbero essere registrati al fine di garantire il rispetto dei termini e delle condizioni del servizio.
            </p>
